#Crie um sistema em Python que mostra a soma e a média aritmética de dois números informados pelo usuário.

#Entrada de dados
n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))

#Processamento
soma = n1 + n2
media = soma / 2

#Saída de Dados
print('Soma:', soma)
print('Média:', media)
